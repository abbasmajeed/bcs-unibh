INSERT INTO CLIENTE
(COD_MATRIZ, NOME_MATRIZ, COD_FILIAL, NOME_FILIAL)
(SELECT FATURAMENTO_RELACIONAL.MATRIZ.COD_MATRIZ,
        FATURAMENTO_RELACIONAL.MATRIZ.NOME_MATRIZ,
        FATURAMENTO_RELACIONAL.FILIAL.COD_FILIAL,
        FATURAMENTO_RELACIONAL.FILIAL.NOME_FILIAL        
   FROM FATURAMENTO_RELACIONAL.MATRIZ, FATURAMENTO_RELACIONAL.FILIAL
  WHERE FATURAMENTO_RELACIONAL.MATRIZ.COD_MATRIZ = FATURAMENTO_RELACIONAL.FILIAL.COD_MATRIZ);
/
INSERT INTO SOFTWARE
(COD_SOFTWARE, NOME_SOFTWARE, COD_FABRICA, NOME_FABRICA)
(SELECT FATURAMENTO_RELACIONAL.SOFTWARE.COD_SOFTWARE,
        FATURAMENTO_RELACIONAL.SOFTWARE.NOME_SOFTWARE,
        FATURAMENTO_RELACIONAL.FABRICANTE.COD_FABRICA,
        FATURAMENTO_RELACIONAL.FABRICANTE.NOME_FABRICA
   FROM FATURAMENTO_RELACIONAL.FABRICANTE,
        FATURAMENTO_RELACIONAL.SOFTWARE
   WHERE FATURAMENTO_RELACIONAL.FABRICANTE.COD_FABRICA = FATURAMENTO_RELACIONAL.SOFTWARE.COD_FABRICA);
/


INSERT INTO FATURAMENTO
(NF, DATA, VLR_TOTAL, IMPOSTO, TX_IMP, COD_MATRIZ, COD_FILIAL)
(SELECT FATURAMENTO_RELACIONAL.FATURAMENTO.NF,
        FATURAMENTO_RELACIONAL.FATURAMENTO.DATA,
        FATURAMENTO_RELACIONAL.FATURAMENTO.VLR_TOTAL,
        FATURAMENTO_RELACIONAL.FATURAMENTO.IMPOSTO,
        FATURAMENTO_RELACIONAL.FATURAMENTO.TX_IMP,
        FATURAMENTO_RELACIONAL.PEDIDO.COD_MATRIZ,
        FATURAMENTO_RELACIONAL.PEDIDO.COD_FILIAL
   FROM FATURAMENTO_RELACIONAL.FATURAMENTO,
        FATURAMENTO_RELACIONAL.PEDIDO
  WHERE FATURAMENTO_RELACIONAL.FATURAMENTO.NF = FATURAMENTO_RELACIONAL.PEDIDO.NF);

